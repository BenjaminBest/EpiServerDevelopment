namespace EpiServerDevelopment.Features.Editors.IconEditor
{
    /// <summary>
    /// The class Icon defines one icon
    /// </summary>
    public class Icon
    {
        /// <summary>
        /// Gets or sets the name.
        /// </summary>
        /// <value>
        /// The name.
        /// </value>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the CSS class.
        /// </summary>
        /// <value>
        /// The CSS class.
        /// </value>
        public string CssClass { get; set; }
    }
}